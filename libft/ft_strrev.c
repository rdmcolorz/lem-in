#include "libft.h"

char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	temp;

	if (str == NULL)
		return (NULL);
	i = 0;
	j = ft_strlen(str) - 1;
	while (str[i] && i < j)
	{
		temp = str[i];
		str[i] = str[j];
		str[j] = temp;
		i++;
		j--;
	}
	return (str);
}
